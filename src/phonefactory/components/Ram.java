package phoneFactory.components;

/**
 *
 * @author Darragh
 */
public class Ram {
 private String ramSize;
 private int gen;

    /**
     * @return the ramSize
     */
    public String getRamSize() {
        return ramSize;
    }

    /**
     * @param ramSize the ramSize to set
     */
    public void setRamSize(String ramSize) {
        this.ramSize = ramSize;
    }

    /**
     * @return the gen
     */
    public int getGen() {
        return gen;
    }

    /**
     * @param gen the gen to set
     */
    public void setGen(int gen) {
        this.gen = gen;
    }
}
