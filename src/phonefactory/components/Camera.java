package phoneFactory.components;

/**
 *
 * @author Darragh
 */
public class Camera {
    private int pixels;
    private String make;
    

    /**
     * @return the pixels
     */
    public int getPixels() {
        return pixels;
    }

    /**
     * @param pixels the pixels to set
     */
    public void setPixels(int pixels) {
        this.pixels = pixels;
    }

    /**
     * @return the make
     */
    public String getMake() {
        return make;
    }

    /**
     * @param make the make to set
     */
    public void setMake(String make) {
        this.make = make;
    }
}
